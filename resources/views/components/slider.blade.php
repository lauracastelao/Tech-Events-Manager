<script>
    document.addEventListener('alpine:init', () => {
        Alpine.data('slider', () => ({
            currentIndex: 1,
            images: [

                    'https://github.com/lauracastelao/image/blob/main/comic.jpg?raw=true',
                    'https://blogbagatela.files.wordpress.com/2020/08/117894587_139394787836666_3702192907280656625_n.jpg',
                    'https://creatorium3d.com/wp-content/uploads/2021/06/juegos-rol-impresion-3d.png',
                    'https://fotografias.lasexta.com/clipping/cmsimages02/2021/05/24/A89FE80F-F749-4DF9-9ACC-01C0FF1167ED/98.jpg?crop=1000,563,x0,y3&width=1900&height=1069&optimize=low&format=webply'
            ],
            back() {
                if (this.currentIndex > 1) {
                    this.currentIndex = this.currentIndex - 1;
                }
            },
            next() {
                if (this.currentIndex < this.images.length) {
                    this.currentIndex = this.currentIndex + 1;
                } else if (this.currentIndex <= this.images.length){
                    this.currentIndex = this.images.length - this.currentIndex + 1
                }
            },
        }))
    })
</script>
